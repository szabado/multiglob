package main

import (
	"flag"
	"github.com/szabado/multiglob"
	"log"
	"os"
	"runtime/pprof"
)

var cpuprofile = flag.String("cpuprofile", "", "write cpu profile to file")

func main() {
	flag.Parse()
	if *cpuprofile != "" {
		f, err := os.Create(*cpuprofile)
		if err != nil {
			log.Fatal(err)
		}
		pprof.StartCPUProfile(f)
		defer pprof.StopCPUProfile()
	}

	builder := multiglob.New()
	for _, pattern := range globFruitPatterns {
		builder.MustAddPattern(pattern, pattern)
	}

	mg := builder.MustCompile()

	for i := 0; i < 2000000; i++ {
		mg.Match("ELDERBERRY,APPLE,GRAPEFRUIT,FIG,CHERRY,BANANA,DATE")
	}
}

